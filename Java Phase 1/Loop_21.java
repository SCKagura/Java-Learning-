public class Loop_21 {
    public static void main(String[] args) {
    
            int i = 0;
            while(i<10)
            {
                System.out.println("สวัสดีรอบที่ "+(i+1));
                i++ ;
            }
            for(int j = 0 ;j < 10 ;j++)
            {
                System.out.println("Hello World รอบที่ "+(j+1));
            }
            int k =0 ;
            do
            {
                System.out.println("SCKagura " + (k+1));
                k++ ;
            }while(k<10) ;
    }
}
